package com.creolin.bar_chart;

public class Tester {


    private String name ;
    private int mark ;

    public Tester(String name, int mark) {
        this.name = name;
        this.mark = mark;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMark() {
        return mark;
    }

    public void setMark(int mark) {
        this.mark = mark;
    }


}
